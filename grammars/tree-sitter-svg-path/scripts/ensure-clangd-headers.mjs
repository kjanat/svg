#!/usr/bin/env node
// @ts-check
import { execFileSync } from 'node:child_process';
import { existsSync, lstatSync, mkdirSync, rmSync, symlinkSync, writeFileSync } from 'node:fs';
import { createRequire } from 'node:module';
import { homedir } from 'node:os';
import { basename, dirname, join } from 'node:path';
import { env, execPath, platform, versions } from 'node:process';
import { fileURLToPath } from 'node:url';

const require = createRequire(import.meta.url);
const grammarRoot = fileURLToPath(new URL('..', import.meta.url));
const repoRoot = join(grammarRoot, '..', '..');
const headersRoot = join(grammarRoot, '.clangd-headers');
const isWindows = platform === 'win32';

/**
 * Replace `.clangd-headers/<name>` with a symlink to `source`.
 * @param {string} name Link name under `.clangd-headers`.
 * @param {string} source Directory the symlink points at.
 * @returns {void}
 */
function linkHeaderDir(name, source) {
	const destination = join(headersRoot, name);
	mkdirSync(headersRoot, { recursive: true });
	if (existsSync(destination) || lstatExists(destination)) {
		rmSync(destination, { recursive: true, force: true });
	}
	symlinkSync(source, destination, isWindows ? 'junction' : 'dir');
}

/**
 * Whether a path exists as a symlink/file/dir without following the link.
 * @param {string} file Path to probe.
 * @returns {boolean}
 */
function lstatExists(file) {
	try {
		lstatSync(file);
		return true;
	} catch {
		return false;
	}
}

/**
 * Resolve a runnable `node-gyp.js` from the installed dependency tree.
 * @returns {string | null} Path to `node-gyp.js`, or `null` when not installed.
 */
function resolveNodeGyp() {
	try {
		return require.resolve('node-gyp/bin/node-gyp.js');
	} catch {
		return null;
	}
}

/**
 * Ensure the Node C headers exist (fetching them via node-gyp if needed) and link them.
 * @returns {void}
 */
function ensureNodeHeaders() {
	const includeDir = join(homedir(), '.cache', 'node-gyp', versions.node, 'include', 'node');
	if (!existsSync(join(includeDir, 'node_api.h'))) {
		const nodeGyp = resolveNodeGyp();
		if (nodeGyp) {
			execFileSync(execPath, [nodeGyp, 'install', '--ensure'], { stdio: 'inherit' });
		}
	}
	if (existsSync(join(includeDir, 'node_api.h'))) {
		linkHeaderDir('node', includeDir);
		return;
	}
	console.warn(`ensure-clangd-headers: missing Node headers at ${includeDir}`);
}

/**
 * Pick a Python interpreter: `$PYTHON`, then the repo `.venv`, then a system default.
 * @returns {string} Interpreter path or command name.
 */
function resolvePython() {
	const configured = env.PYTHON;
	if (configured && existsSync(configured)) return configured;

	const venvPython = join(repoRoot, '.venv', isWindows ? 'Scripts' : 'bin', isWindows ? 'python.exe' : 'python3');
	if (existsSync(venvPython)) return venvPython;

	return isWindows ? 'python' : 'python3';
}

/**
 * Ask an interpreter for its C include directory.
 * @param {string} python Interpreter path or command name.
 * @returns {string | null} The include directory, or `null` when it cannot be determined.
 */
function pythonIncludeDir(python) {
	try {
		return execFileSync(
			python,
			['-c', 'import sysconfig; print(sysconfig.get_path("include") or "")'],
			{ encoding: 'utf8' },
		).trim() || null;
	} catch {
		return null;
	}
}

/**
 * Locate `Python.h` and link its directory into `.clangd-headers`.
 * @returns {void}
 */
function ensurePythonHeaders() {
	const python = resolvePython();
	const includeDir = pythonIncludeDir(python);
	if (includeDir && existsSync(join(includeDir, 'Python.h'))) {
		linkHeaderDir('python', includeDir);
		return;
	}
	console.warn(`ensure-clangd-headers: could not find Python.h using ${python}`);
}

ensureNodeHeaders();
ensurePythonHeaders();

/**
 * Resolve the `node-addon-api` include directory, if installed.
 * @returns {string | null} The package root (which holds the headers), or `null`.
 */
function nodeAddonApiInclude() {
	try {
		return dirname(require.resolve('node-addon-api/package.json'));
	} catch {
		return null;
	}
}

/**
 * Write the generated `.clangd` compile-flags config for this grammar's C scanner.
 * @returns {void}
 */
function writeClangdConfig() {
	const bindingName = `${basename(grammarRoot).replace(/-/g, '_')}_binding`;
	const includes = [join(grammarRoot, 'src')];
	const addon = nodeAddonApiInclude();
	if (addon) {
		includes.push(addon);
	}
	includes.push(join(headersRoot, 'node'), join(headersRoot, 'python'));

	const lines = [
		'# Generated by scripts/ensure-clangd-headers.mjs. Do not edit; gitignored.',
		'CompileFlags:',
		'  Add:',
		...includes.map((path) => `    - -I${path}`),
		`    - -DNODE_GYP_MODULE_NAME=${bindingName}`,
		'    - -DNAPI_CPP_EXCEPTIONS',
		'    - -DBUILDING_NODE_EXTENSION',
		'',
	];
	writeFileSync(join(grammarRoot, '.clangd'), `${lines.join('\n')}\n`);
}

writeClangdConfig();
